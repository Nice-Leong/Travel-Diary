// 模拟获取游记列表的 API
export const mockGetTravelNotes = ({ page, pageSize, searchKey }) => {
  return new Promise((resolve) => {
    setTimeout(() => {
      let data = JSON.parse(localStorage.getItem('mockTravelNotes') || '[]');
      
      // 搜索过滤
      if (searchKey) {
        data = data.filter(item => 
          item.title.includes(searchKey) || 
          item.author.nickname.includes(searchKey)
        );
      }
      
      // 分页处理
      const start = (page - 1) * pageSize;
      const end = start + pageSize;
      const paginatedData = data.slice(start, end);
      
      // 判断是否还有更多数据
      const hasMore = data.length > end;
      
      resolve({
        code: 200,
        message: 'success',
        data: {
          list: paginatedData,
          total: data.length,
          hasMore
        }
      });
    }, 500); // 模拟网络延迟
  });
};

